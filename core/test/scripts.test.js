test('R: no unreplaced tokens or leaked JS values in either state', () => {
  for (const state of [FIXTURE, minimalState()]) {
    for (const script of [generateRScript(state), generatePyGMT(state)]) {
      assert.ok(!script.includes('WIDTH'), 'no WIDTH token');
      assert.ok(!script.includes('undefined'), 'no leaked undefined');
      assert.ok(!script.includes('NaN'), 'no leaked NaN');
      assert.ok(!script.includes('${'), 'no unexpanded template literal');
      assert.ok(!script.includes('[object Object]'), 'no stringified object');
    }
  }
});

