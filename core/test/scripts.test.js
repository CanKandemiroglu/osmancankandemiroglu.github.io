/* ------------------------------------------------------------- shared ---- */

test('both generators are deterministic (byte-identical on repeat calls)', () => {
  assert.equal(generatePyGMT(FIXTURE), generatePyGMT(FIXTURE));
  assert.equal(generateRScript(FIXTURE), generateRScript(FIXTURE));
});

test('both generators reject malformed states', () => {
  for (const gen of [generatePyGMT, generateRScript]) {
    assert.throws(() => gen(null), TypeError);
    assert.throws(() => gen({}), TypeError);

    const noWidth = structuredClone(FIXTURE);
    noWidth.projection.gmt = '-JM16c'; // already substituted — not a template
    assert.throws(() => gen(noWidth), TypeError, 'missing WIDTH token');

    const badRegion = structuredClone(FIXTURE);
    badRegion.region.north = Number.NaN;
    assert.throws(() => gen(badRegion), TypeError);

    const inverted = structuredClone(FIXTURE);
    inverted.region.south = 50; // south >= north
    assert.throws(() => gen(inverted), RangeError);
  }
});

test('committed sample scripts in /scripts are in sync with the generators', () => {
  const scriptsDir = path.resolve(import.meta.dirname, '..', '..', 'scripts');
  const pyPath = path.join(scriptsDir, 'example-figure.py');
  const rPath = path.join(scriptsDir, 'example-figure.R');
  assert.ok(fs.existsSync(pyPath), `${pyPath} missing — regenerate per scripts/README.md`);
  assert.ok(fs.existsSync(rPath), `${rPath} missing — regenerate per scripts/README.md`);
  assert.equal(
    fs.readFileSync(pyPath, 'utf8'),
    generatePyGMT(FIXTURE),
    'example-figure.py is stale — regenerate it from the fixture (see scripts/README.md)',
  );
  assert.equal(
    fs.readFileSync(rPath, 'utf8'),
    generateRScript(FIXTURE),
    'example-figure.R is stale — regenerate it from the fixture (see scripts/README.md)',
  );
});
